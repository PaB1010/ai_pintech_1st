const changeFruit = (x) => {
  const rooms = {
    원룸: "🍉",
    오피스텔: "🍋",
    투룸: "🍅",
    아파트: "🍌",
  };
  return rooms[x];
};

const descChange = (x) => {
  const arr = x.split(", ");
  return arr[0] + " " + arr[2];
};

const roomCostChange = (x) => {
  const arr = x.split(", ");
  const cost = parseInt(arr[2].split(" ")[1]); //7
  return cost > 5 ? "관리비 비쌈" : "관리비 저렴";
};


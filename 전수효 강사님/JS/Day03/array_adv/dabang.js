const newArray = rl.map((x) => {
  const newObj = {};
  newObj.roomTypeName = changeFruit(x.roomTypeName);
  newObj.roomTitle = x.roomTitle;
  newObj.roomDesc = descChange(x.roomDesc);
  newObj.roomCost = roomCostChange(x.roomDesc);
  newObj.priceTitle = x.priceTitle;
  newObj.priceTypeName = x.priceTypeName;
  return newObj;
});

